class ReActAgentState(TypedDict):
    """State schema for the ReAct agent pipeline."""

    # Video info
    video_path: str
    video_url: str
    video_info: dict
    output_dir: str

    # Shared context
    config: dict
    qwen_client: Any
    skills_dir: str
    previous_preds: dict

    # ReAct agent output
    category_results: dict          # category -> {pred, events, ...}
    on_step: Any                    # Optional callback for interactive output

    # Legacy compatibility (for output_formatter)
    merged_simple: dict
    simple_preds: Annotated[list[dict], operator.add]
    simple_traces: Annotated[list[dict], operator.add]
    agent_outputs: Annotated[list[dict], operator.add]
    complex_preds: Annotated[list[dict], operator.add]
    complex_traces: Annotated[list[dict], operator.add]
    complex_direct_results: dict
    expected_complex_slices: int
    # Final output
    final_output: dict


def build_react_video_analysis_graph() -> StateGraph:
    """Build the ReAct-based LangGraph StateGraph.

    Simple topology: root_agent → output_formatter → END

    The root_agent internally handles all coordination via
    spawn_sub_agent, query_result, and review_result tools.
    """
    workflow = StateGraph(ReActAgentState)

    workflow.add_node("root_agent", root_agent_node)
    workflow.add_node("output_formatter", output_formatter_node)

    workflow.set_entry_point("root_agent")
    workflow.add_edge("root_agent", "output_formatter")
    workflow.add_edge("output_formatter", END)

    return workflow


