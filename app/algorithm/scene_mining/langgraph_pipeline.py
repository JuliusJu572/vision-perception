"""
LangGraph Multi-Agent Video Analysis Pipeline

Architecture:
[START] → [supervisor_1] → Fan-out(simple_workers) → [simple_reducer] → [supervisor_2] → Fan-out(complex_workers) → [output_formatter] → [END]

Uses Send API for dynamic fan-out/fan-in parallelism.
Supervisor only receives pred fields, not full reasoning.
"""

import asyncio
import datetime
import logging
import shutil
from pathlib import Path
from typing import Annotated, Any, TypedDict
import operator

from langgraph.types import Send
from langgraph.graph import END, StateGraph

from nodes import (
    complex_worker_node,
    output_formatter_node,
    root_agent_node,
    simple_reducer_node,
    simple_worker_node,
    supervisor_1_node,
    supervisor_2_node,
)

logger = logging.getLogger(__name__)


def build_video_url_from_config(config: dict, video_path: str) -> str:
    prefix = str(config.get("paths", {}).get("video_url_prefix", "file:///app/videos")).rstrip("/")
    return f"{prefix}/{video_path}"


class AgentState(TypedDict):
    """State schema for the LangGraph pipeline."""

    # Video info
    video_path: str
    video_url: str
    video_info: dict  # {"duration": float, "fps": int}
    output_dir: str

    # Shared context
    config: dict
    qwen_client: Any  # QwenClient instance
    skills_dir: str

    # Previous preds from external context (e.g., previous video's results)
    previous_preds: dict

    # Stage 1: Simple tasks
    simple_tasks: list[dict]  # Generated by supervisor_1
    simple_preds: Annotated[list[dict], operator.add]  # Output from simple workers (Send API accumulates here)
    simple_traces: Annotated[list[dict], operator.add]  # Trace from simple workers
    agent_outputs: Annotated[list[dict], operator.add]

    # Stage 2: Complex tasks
    merged_simple: dict  # Reducer output
    complex_tasks: list[dict]  # Generated by supervisor_2
    complex_direct_results: dict
    complex_preds: Annotated[list[dict], operator.add]  # Output from complex workers (Send API accumulates here)
    complex_traces: Annotated[list[dict], operator.add]  # Trace from complex workers
    expected_complex_slices: int

    # Final output
    final_output: dict


def _cleanup_tool_clips(video_root: str, video_path: str, config: dict | None = None) -> None:
    """Delete temporary clip files generated during pipeline execution."""
    paths_cfg = (config or {}).get("paths", {})
    configured_root = paths_cfg.get("tool_clip_root")
    if not configured_root:
        configured_root = Path(paths_cfg.get("output_base", "/app/outputs/scene_mining")) / "_tool_clips"
    candidate_roots = [Path(configured_root), Path(video_root) / "_tool_clips"]
    video_stem = Path(video_path).stem
    date_str = datetime.date.today().strftime("%Y%m%d")
    for clips_root in candidate_roots:
        video_clip_dir = clips_root / date_str / video_stem
        if not video_clip_dir.exists():
            continue
        try:
            shutil.rmtree(video_clip_dir)
            logger.info("Cleaned up tool clips: %s", video_clip_dir)
        except Exception as e:
            logger.warning("Failed to clean up tool clips %s: %s", video_clip_dir, e)


def get_video_info(video_path: str) -> dict:
    """Get video duration and fps using ffprobe."""
    import subprocess
    import json

    try:
        result = subprocess.run(
            [
                "ffprobe", "-v", "error",
                "-select_streams", "v:0",
                "-show_entries", "stream=duration,r_frame_rate",
                "-of", "json",
                video_path
            ],
            capture_output=True,
            text=True,
            timeout=30,
        )
        data = json.loads(result.stdout or "{}")
        streams = data.get("streams", [{}])
        stream = streams[0] if streams else {}

        duration = float(stream.get("duration", 0) or 0)
        fps_str = stream.get("r_frame_rate", "30/1")
        if "/" in fps_str:
            num, den = fps_str.split("/")
            fps = int(float(num) / float(den)) if float(den) != 0 else 30
        else:
            fps = int(fps_str)

        return {"duration": duration, "fps": fps}
    except Exception as e:
        logger.warning("Failed to get video info for %s: %s", video_path, e)
        return {"duration": 0, "fps": 1}


# =============================================================================
# Conditional Edge Functions (Fan-out)
# =============================================================================

def fan_out_simple_workers(state: AgentState) -> list[Send]:
    """
    Conditional edge: supervisor_1 → simple_workers (parallel fan-out).

    Returns a list of Send objects, one per simple task.
    """
    simple_tasks = state.get("simple_tasks", [])
    if not simple_tasks:
        logger.warning("No simple tasks to fan-out")
        return []

    sends = []
    for task in simple_tasks:
        send = Send(
            "simple_worker",
            {
                "task": task,
                "video_path": state["video_path"],
                "video_url": state["video_url"],
                "video_info": state["video_info"],
                "output_dir": state["output_dir"],
                "qwen_client": state["qwen_client"],
                "config": state["config"],
                "skills_dir": state["skills_dir"],
                "on_step": state.get("on_step"),
            },
        )
        sends.append(send)

    logger.info("Fan-out to %d simple workers", len(sends))
    return sends


def _build_output_formatter_bypass(state: AgentState) -> list[Send]:
    """Build a Send directly to output_formatter, bypassing complex workers."""
    return [
        Send(
            "output_formatter",
            {
                "merged_simple": state.get("merged_simple", {}),
                "complex_preds": state.get("complex_preds", []),
                "complex_direct_results": state.get("complex_direct_results", {}),
                "simple_traces": state.get("simple_traces", []),
                "complex_traces": state.get("complex_traces", []),
                "agent_outputs": state.get("agent_outputs", []),
                "output_dir": state.get("output_dir", ""),
                "complex_tasks": state.get("complex_tasks", []),
            },
        )
    ]


def fan_out_complex_workers(state: AgentState) -> list[Send]:
    """
    Conditional edge: supervisor_2 → complex_workers (parallel fan-out).

    Returns a list of Send objects, one per complex task.
    """
    complex_tasks = state.get("complex_tasks", [])
    if not complex_tasks:
        logger.info("No complex tasks to fan-out, route directly to output_formatter")
        return _build_output_formatter_bypass(state)

    sends = []
    for task in complex_tasks:
        slices = task.get("time_slices")
        if not isinstance(slices, list) or not slices:
            maybe_single = task.get("time_slice")
            slices = [maybe_single] if isinstance(maybe_single, dict) else []
        if not slices:
            continue
        for idx, one_slice in enumerate(slices):
            if not isinstance(one_slice, dict):
                continue
            sub_agent_id = f"{task.get('category', 'unknown')}_slice_{idx + 1}"
            send = Send(
                "complex_worker",
                {
                    "task": task,
                    "time_slice": one_slice,
                    "slice_idx": idx,
                    "sub_agent_id": sub_agent_id,
                    "video_path": state["video_path"],
                    "video_url": state["video_url"],
                    "video_info": state["video_info"],
                    "output_dir": state["output_dir"],
                    "qwen_client": state["qwen_client"],
                    "config": state["config"],
                    "skills_dir": state["skills_dir"],
                    "merged_simple": state.get("merged_simple", {}),
                    "on_step": state.get("on_step"),
                },
            )
            sends.append(send)

    if not sends:
        logger.info("Complex tasks exist but no valid slices, route directly to output_formatter")
        return _build_output_formatter_bypass(state)
    logger.info("Fan-out to %d complex workers", len(sends))
    return sends


# =============================================================================
# Graph Construction
# =============================================================================

def build_video_analysis_graph() -> StateGraph:
    """Build the LangGraph StateGraph for video analysis."""

    workflow = StateGraph(AgentState)

    # Add nodes
    workflow.add_node("supervisor_1", supervisor_1_node)
    workflow.add_node("simple_reducer", simple_reducer_node)
    workflow.add_node("supervisor_2", supervisor_2_node)
    workflow.add_node("simple_worker", simple_worker_node)  # Used via Send API
    workflow.add_node("complex_worker", complex_worker_node)  # Used via Send API
    workflow.add_node("output_formatter", output_formatter_node)

    # Set entry point
    workflow.set_entry_point("supervisor_1")

    # Edge: supervisor_1 → simple_workers (conditional fan-out via Send API)
    workflow.add_conditional_edges(
        "supervisor_1",
        fan_out_simple_workers,
    )

    # Edge: simple_workers → simple_reducer (fan-in)
    workflow.add_edge("simple_worker", "simple_reducer")

    # Edge: simple_reducer → supervisor_2
    workflow.add_edge("simple_reducer", "supervisor_2")

    # Edge: supervisor_2 → complex_workers (conditional fan-out via Send API)
    workflow.add_conditional_edges(
        "supervisor_2",
        fan_out_complex_workers,
    )

    # Edge: complex_workers → output_formatter (direct fan-in; complex_reducer removed)
    workflow.add_edge("complex_worker", "output_formatter")

    # Edge: output_formatter → END
    workflow.add_edge("output_formatter", END)

    return workflow


_compiled_graph = None
_compiled_react_graph = None


def _get_compiled_graph():
    """Return the compiled DAG graph, building it once on first call."""
    global _compiled_graph
    if _compiled_graph is None:
        _compiled_graph = build_video_analysis_graph().compile()
        logger.info("LangGraph pipeline compiled (once per process)")
    return _compiled_graph


def _get_compiled_react_graph():
    """Return the compiled ReAct graph, building it once on first call."""
    global _compiled_react_graph
    if _compiled_react_graph is None:
        _compiled_react_graph = build_react_video_analysis_graph().compile()
        logger.info("ReAct LangGraph pipeline compiled (once per process)")
    return _compiled_react_graph


