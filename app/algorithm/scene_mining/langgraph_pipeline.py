async def run_video_analysis(
    video_path: str,
    video_root: str,
    output_dir: str,
    qwen_client,
    config: dict,
    skills_dir: str,
    previous_preds: dict | None = None,
    on_step=None,
) -> dict:
    """
    Run the LangGraph pipeline for a single video.

    Args:
        video_path: Relative path within video_root
        video_root: Root directory for videos
        output_dir: Output directory for this video's results
        qwen_client: Initialized QwenClient
        config: Full config dict
        skills_dir: Path to skills directory
        previous_preds: Optional dict of predictions from prior context

    Returns:
        final_output: dict with all category results
    """
    # Build full video path
    full_video_path = str(Path(video_root) / video_path)
    video_url = build_video_url_from_config(config, video_path)

    # Get video info
    video_info = get_video_info(full_video_path)

    # Ensure output dir exists
    video_output_dir = Path(output_dir) / Path(video_path).stem
    video_output_dir.mkdir(parents=True, exist_ok=True)

    # Initial state
    initial_state: AgentState = {
        "video_path": video_path,
        "video_url": video_url,
        "video_info": video_info,
        "output_dir": str(video_output_dir),
        "config": config,
        "qwen_client": qwen_client,
        "skills_dir": skills_dir,
        "on_step": on_step,
        "previous_preds": previous_preds or {},
        "simple_tasks": [],
        "simple_preds": [],
        "simple_traces": [],
        "agent_outputs": [],
        "merged_simple": {},
        "complex_tasks": [],
        "complex_direct_results": {},
        "complex_preds": [],
        "complex_traces": [],
        "expected_complex_slices": 0,
        "final_output": {},
    }

    # Build and compile graph (reuses module-level compiled graph)
    compiled = _get_compiled_graph()

    # Run
    if on_step:
        on_step("vlm", "VLM分析中...", {"phase": "simple"})
    logger.info("Starting LangGraph pipeline for video: %s", video_path)
    result = await compiled.ainvoke(initial_state)

    # result is the final state
    final_output = result.get("final_output", {})

    logger.info("LangGraph pipeline completed for %s: %d categories",
                video_path, len(final_output))

    # Clean up temporary clip files generated during this video's pipeline run
    _cleanup_tool_clips(video_root, video_path, config)

    return final_output


async def run_video_analysis_streaming(
    video_path: str,
    video_root: str,
    output_dir: str,
    qwen_client,
    config: dict,
    skills_dir: str,
    previous_preds: dict | None = None,
):
    """
    Streaming version of run_video_analysis.

    Yields intermediate states for progress tracking.
    """
    full_video_path = str(Path(video_root) / video_path)
    video_url = build_video_url_from_config(config, video_path)
    video_info = get_video_info(full_video_path)

    video_output_dir = Path(output_dir) / Path(video_path).stem
    video_output_dir.mkdir(parents=True, exist_ok=True)

    initial_state: AgentState = {
        "video_path": video_path,
        "video_url": video_url,
        "video_info": video_info,
        "output_dir": str(video_output_dir),
        "config": config,
        "qwen_client": qwen_client,
        "skills_dir": skills_dir,
        "previous_preds": previous_preds or {},
        "simple_tasks": [],
        "simple_preds": [],
        "simple_traces": [],
        "agent_outputs": [],
        "merged_simple": {},
        "complex_tasks": [],
        "complex_direct_results": {},
        "complex_preds": [],
        "complex_traces": [],
        "expected_complex_slices": 0,
        "final_output": {},
    }

    compiled = _get_compiled_graph()

    try:
        async for state in compiled.astream(initial_state):
            yield state
    finally:
        _cleanup_tool_clips(video_root, video_path, config)


